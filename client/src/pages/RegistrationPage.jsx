  const renderPdfToImage = async (file, password = '') => {
    const arrayBuffer = await file.arrayBuffer();
    const loadingTask = pdfjsLib.getDocument({
      data: new Uint8Array(arrayBuffer),
      ...(password ? { password } : {})
    });
    const pdf = await loadingTask.promise;
    const page = await pdf.getPage(1);

    // Render at 2x scale for better OCR quality
    const scale = 2.0;
    const viewport = page.getViewport({ scale });
    const canvas = document.createElement('canvas');
    canvas.width = viewport.width;
    canvas.height = viewport.height;
    const ctx = canvas.getContext('2d');

    await page.render({ canvasContext: ctx, viewport }).promise;

    // Convert to greyscale + boost contrast for OCR
    const imageData = ctx.getImageData(0, 0, canvas.width, canvas.height);
    const data = imageData.data;
    for (let i = 0; i < data.length; i += 4) {
      const grey = 0.299 * data[i] + 0.587 * data[i+1] + 0.114 * data[i+2];
      const boosted = ((grey - 128) * 1.5) + 128;
      const clamped = Math.max(0, Math.min(255, boosted));
      data[i] = data[i+1] = data[i+2] = clamped;
    }
    ctx.putImageData(imageData, 0, 0);

    return canvas.toDataURL('image/png');
  };

