function calculateAge(dob) {
  if (!dob) return '';
  const today = new Date();
  const birthDate = new Date(dob);
  let age = today.getFullYear() - birthDate.getFullYear();
  const m = today.getMonth() - birthDate.getMonth();
  if (m < 0 || (m === 0 && today.getDate() < birthDate.getDate())) age--;
  return age;
}

// Document Verification Report Component
// Handles both structured JSON data AND raw text fallback
function ReviewReport({ data }) {
  if (!data) return null;

  // If we got raw text (JSON parse failed), render it nicely
  if (data._isRawText) {
    return (
      <div className="report-card animate-fade-in">
        <div className="report-header">
          <div>
            <h3>📋 Application Verification Report</h3>
            <p className="report-meta">Generated by CivicQ AI — Form 6 Review</p>
          </div>
        </div>
        <div className="report-narrative raw-report">
          {data.rawText.split('\n').map((line, i) => {
            if (!line.trim()) return <br key={i} />;
            if (line.startsWith('###')) return <h4 key={i}>{line.replace(/^#+\s*/, '')}</h4>;
            if (line.startsWith('##')) return <h3 key={i}>{line.replace(/^#+\s*/, '')}</h3>;
            if (line.startsWith('* **') || line.startsWith('- **')) {
              const match = line.match(/[*-]\s*\*\*(.+?)\*\*:?\s*(.*)/);
              if (match) return <p key={i}><strong>{match[1]}:</strong> {match[2]}</p>;
            }
            if (line.startsWith('* ') || line.startsWith('- ')) {
              return <p key={i} className="report-bullet">• {line.replace(/^[*-]\s*/, '')}</p>;
            }
            if (line.match(/^\d+\.\s/)) {
              return <p key={i} className="report-bullet">{line}</p>;
            }
            return <p key={i}>{line}</p>;
          })}
        </div>
      </div>
    );
  }

  // Structured JSON report
  const scoreColor = data.overallScore >= 8 ? '#22c55e' : data.overallScore >= 5 ? '#f59e0b' : '#ef4444';

  return (
    <div className="report-card animate-fade-in">
      <div className="report-header">
        <div>
          <h3>📋 Application Verification Report</h3>
          <p className="report-meta">Generated by CivicQ AI — Form 6 Review</p>
        </div>
        <div className="report-score" style={{ '--score-color': scoreColor }}>
          <span className="score-num">{data.overallScore}</span>
          <span className="score-denom">/10</span>
        </div>
      </div>

      {data.eligibility && (
        <div className={`eligibility-banner ${data.eligibility.ageEligible ? 'eligible' : 'not-eligible'}`}>
          <span className="elig-icon">{data.eligibility.ageEligible ? '✅' : '❌'}</span>
          <div>
            <strong>Age Eligibility</strong>
            <span>{data.eligibility.note}</span>
          </div>
        </div>
      )}

      {data.documentSummary && (
        <div className="doc-detection">
          <div className="doc-detection-header">
            <span>📄</span>
            <strong>Document Analysis</strong>
          </div>
          <p>{data.documentSummary}</p>
        </div>
      )}

      {data.verificationChecks && data.verificationChecks.length > 0 && (
        <div className="verification-section">
          <h4>🔍 Document vs. Form Verification</h4>
          <div className="verification-table">
            <div className="vt-header">
              <span>Field</span>
              <span>You Entered</span>
              <span>Found in Document</span>
              <span>Status</span>
            </div>
            {data.verificationChecks.map((check, i) => (
              <div key={i} className={`vt-row ${check.match ? 'match' : 'mismatch'}`}>
                <span className="vt-field">{check.field}</span>
                <span className="vt-value">{check.formValue || '—'}</span>
                <span className="vt-value">{check.docValue || 'Not found'}</span>
                <span className="vt-status">{check.match ? '✅' : '❌'}</span>
              </div>
            ))}
          </div>
        </div>
      )}

      {data.issues && data.issues.length > 0 && (
        <div className="report-issues">
          <h4>⚠️ Issues Found</h4>
          <ul>
            {data.issues.map((issue, i) => <li key={i}>{issue}</li>)}
          </ul>
        </div>
      )}

      {data.recommendations && data.recommendations.length > 0 && (
        <div className="report-recs">
          <h4>💡 Recommendations</h4>
          <ul>
            {data.recommendations.map((rec, i) => <li key={i}>{rec}</li>)}
          </ul>
        </div>
      )}

      {data.summary && (
        <div className="report-narrative">
          <h4>📝 Summary</h4>
          <p>{data.summary}</p>
        </div>
      )}

      <div className="report-verdict">
        <span className={`verdict-badge verdict-${data.verdict || 'review'}`}>
          {data.verdict === 'ready' ? '✅ Ready to Submit'
            : data.verdict === 'incomplete' ? '❌ Incomplete — Please fix issues'
            : '⚠️ Needs Review'}
        </span>
      </div>
    </div>
  );
}

